public class Circle {
	private int radius, x, y;

	public Circle() {
		radius = 10;
		x = 20;
		y = 20;
	}

	public Circle(int r, int a, int b) {
		radius = r;
		x = a;
		y = b;
	}

	public int getRadius() {
		return radius;
	}

	public void setRadius(int r) {
		radius = r;
	}

	public int getX() {
		return x;
	}

	public void setX(int a) {
		x = a;
	}

	public int getY() {
		return y;
	}

	public void setY(int b) {
		y = b;
	}
}


