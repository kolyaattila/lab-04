package ex1;

public class MyCharacter {

	private String name;
	private String color;
	private int numberEye;
	private int numberLife;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public int getNumberEye() {
		return numberEye;
	}

	public void setNumberEye(int numberEye) {
		this.numberEye = numberEye;
	}

	public int getNumberLife() {
		return numberLife;
	}

	public void setNumberLife(int numberLife) {
		this.numberLife = numberLife;
	}
}
